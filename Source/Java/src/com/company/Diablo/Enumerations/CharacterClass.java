/**
 * The CharacterClass-enum defines the classes available, when creating a character.
 * Character classes have different attributes and abilities.
 */
public enum CharacterClass {
    /** The Rogue is a versatile bow and spear-fighter. */
    Rogue,
    /** The Sorcerer uses magic spells to defeat enemies. */
    Sorcerer,
    /** The Warrior is a strong man, who uses swords and axes. */
    Warrior
}
